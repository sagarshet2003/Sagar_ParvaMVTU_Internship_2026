package datatype_variables;

public class primitive_datatypes {
	public static void main(String[] args) {
		//byte
		/* byte age;
		 * age = 30;
		 */
		byte age = 30;  //same line declaration and initialization
		System.out.println("age is :" + age);
		
		short age1 = 20;
		System.out.println("age1 is :" + age1);
		
		int age2 = 20;
		System.out.println("age2 is :" + age2);
		
		long age3 = 20;
		System.out.println("age3 is :" + age3);
		
		float age4 = 20;
		System.out.println("age4 is :" + age4);
		
		double age5 = 30;
		System.out.println("double of age5 is : "+ age5);
		
		char age6 = 50;
		System.out.println("char of age6 is : "+ age6);
		
		boolean age7 = true;
		System.out.println("age7 bool is: " + age7);
		
		
		short var1 = 40;
		System.out.println("short: "+ var1);
		
		int total = 60;
		System.out.println("int :"+total);
		
		long var2 = 4000;
		System.out.println("long "+ var2);
		
		float marks = 20.6f;
		System.out.println("float :"+marks);
		
		double marks1 = 120.50;
		System.out.println("double:"+marks1);
		
		char gender = 'M';
		System.out.println("char:"+gender);
		
		boolean isActive = false;
		System.out.println("boolean:"+isActive);
		
	}
}
