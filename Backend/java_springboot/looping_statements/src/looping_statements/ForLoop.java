package looping_statements;

public class ForLoop {
	public static void main(String[] args) {
		/*
		 * for:
		 * =====
		 * for(ini;con;ice/dec){
		 * code
		 * }
		 * if you know no of iterations
		 */
		int num =5;
		for(int i = 1; i<=10; i++) {
			System.out.println(num+" * "+i+" = "+(num*i));
		}
	}
}
